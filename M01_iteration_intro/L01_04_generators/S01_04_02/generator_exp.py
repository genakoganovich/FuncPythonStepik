# Плохой вариант: создает в памяти огромный список, а потом его суммирует
# Может привести к ошибке MemoryError
# total_sum = sum([x * x for x in range(1_000_000)])

# Отличный, эффективный вариант:
# Генераторное выражение создает по одному квадрату за раз,
# а функция sum() сразу же его "потребляет" и прибавляет к общей сумме.
# В памяти никогда не хранится больше одного числа!
total_sum = sum(x * x for x in range(1_000_000))

print(f"Сумма рассчитана, память в безопасности. Результат: {total_sum}")