def square(number):
  """Эта функция возвращает квадрат числа."""
  return number * number

# Вызываем функцию как обычно
result = square(5)
print(f"Обычный вызов: {result}")

# А теперь самое интересное!
# Присваиваем саму функцию (без скобок!) новой переменной.
# Мы не вызываем square(), а ссылаемся на сам объект функции.
f = square

# Давайте посмотрим, что теперь хранится в переменных square и f.
print(f"Что такое square? {square}")
print(f"Что такое f? {f}")

# Как видите, обе переменные ссылаются на один и тот же объект функции!
# Теперь мы можем вызвать эту функцию через новую переменную f.
new_result = f(5)
print(f"Вызов через новую переменную: {new_result}")