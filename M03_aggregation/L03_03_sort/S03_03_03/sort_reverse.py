numbers_to_sort_inplace = [30, 10, 50, 20, 40]
print(f"Список до сортировки: {numbers_to_sort_inplace}")

numbers_to_sort_inplace.sort(reverse=True)

print(f"Список после обратной сортировки на месте: {numbers_to_sort_inplace}")
# Вывод: Список после обратной сортировки на месте: [50, 40, 30, 20, 10]