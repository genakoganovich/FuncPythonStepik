words = ['вишня', 'арбуз', 'яблоко', 'банан']

# Сортируем по результату, который вернет len() для каждого слова
sorted_by_length = sorted(words, key=len)

print(sorted_by_length)
# Вывод: ['арбуз', 'вишня', 'банан', 'яблоко']
# (слова "вишня" и "банан" имеют одинаковую длину, их относительный порядок сохраняется)